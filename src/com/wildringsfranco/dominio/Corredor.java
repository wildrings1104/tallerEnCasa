package com.wildringsfranco.dominio;
public class Corredor{
    private String nombreWF;
    private String nacionalidadWF;
    private String generoWF;


    public Corredor(String nombreWF){
        this.nombreWF = nombreWF;
    }

    public String getNombreWF() {
        return nombreWF;
    }

    public String getNacionalidadWF() {
        return nacionalidadWF;
    }

    public String getGeneroWF() {
        return generoWF;
    }
}


